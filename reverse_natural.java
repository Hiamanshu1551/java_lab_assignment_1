import java.util.*;

public class reverse_natural {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int n;
        int i=1;
        System.out.println("enter the range");
        n=sc.nextInt();
        while(n>=i){
            System.out.println(n);
            n--;

        }

        
    }
    
}
